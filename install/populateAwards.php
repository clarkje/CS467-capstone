function buildAward($addresses, $awardTypes, $granteeData, $users) {

  $award = new Award();

  // Set a random award type
  $award->setAwardType($awardTypes[mt_rand(0, sizeof($awardTypes) - 1)]);

  // Generate a random index
  $index = mt_rand(0, sizeof($addresses) - 1);

  // Throw a big spike in the distribution
  if($index % 10 == 0) {
    $index = mt_rand((.5 * sizeof($addresses)), (.6 * sizeof($addresses)));
  }

  // Throw a big spike in the distribution
  if($index % 9 == 0) {
    $index = mt_rand((.1 * sizeof($addresses)), (.2 * sizeof($addresses)));
  }

  $award->setRecipientAddress($addresses[$index]);

  // Set a random granter
  $award->setGranter($users[mt_rand(0, sizeof($users) - 1)]);

  $award->setRecipientFirst($granteeData['firstName']);
  $award->setRecipientLast($granteeData['lastName']);
  $award->setRecipientEmail(strtolower($granteeData['lastName']) . strtolower(substr($granteeData['firstName'], 0, 1)) . "@phoenix.jeromie.com");

  // Generate a date in the last 24 months
  $date = new DateTime();
  date_default_timezone_set('America/Los_Angeles');
  $ts = time();
  $ts = $ts - mt_rand(0, (3.154 * 10^7));
  $date->setTimestamp($ts);

  $award->setGrantDate($date);
  return $award;
}

